boundary_error = [6.8, 9.0, 13.5, 5.2, 7.9]  # False localization error %

x = np.arange(len(categories))
width = 0.25  # Bars ki thikness

# --- GRAPH 1: CROP & ANIMAL MASK LOCALIZATION ACCURACY ---
fig, ax = plt.subplots(figsize=(13, 6))

# Teenon segmentation parameters ke bars plot karna
bar1 = ax.bar(x - width, iou_scores, width, label='Intersection over Union (IoU %)', color='#00a896')
bar2 = ax.bar(x, pixel_accuracy, width, label='Pixel Accuracy (Dice %)', color='#028090')
bar3 = ax.bar(x + width, boundary_error, width, label='Mask Edge Error % (False Alarms)', color='#f25c54')

# Layout, Labels aur Aesthetics set karna
ax.set_title('📐 Multi-Crop Guard System: Segmentation Mask Performance Graph', fontsize=15, fontweight='bold', pad=20)
ax.set_xlabel('Field Types & Isolated Targets', fontsize=12, labelpad=10)
ax.set_ylabel('Performance Score Percentage (%)', fontsize=12, labelpad=10)
ax.set_xticks(x)
ax.set_xticklabels(categories, fontsize=11, fontweight='bold')
ax.set_ylim(0, 115)
ax.legend(loc='upper right', fontsize=10, frameon=True)
ax.grid(axis='y', linestyle='--', alpha=0.4)

# Har individual bar ke upar uska numeric text display karne ka function
def add_value_labels(bars):
    for bar in bars:
        height = bar.get_height()
        ax.annotate(f'{height:.1f}%',
                    xy=(bar.get_x() + bar.get_width() / 2, height),
                    xytext=(0, 3),  # 3 pixels vertical offset
                    textcoords="offset points",
                    ha='center', va='bottom', fontsize=9, fontweight='bold')

add_value_labels(bar1)
add_value_labels(bar2)
add_value_labels(bar3)

plt.tight_layout()
plt.show()

