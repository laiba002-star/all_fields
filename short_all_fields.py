def process_universal_video(input_video_path):
    cap = cv2.VideoCapture(input_video_path)
    fps = int(cap.get(cv2.CAP_PROP_FPS))
    width, height = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH)), int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    out_path = "processed_universal_guard.mp4"
    out = cv2.VideoWriter(out_path, cv2.VideoWriter_fourcc(*'mp4v'), fps, (width, height))

    print(f"🎬 Executing Multi-Crop Intelligent Video Render...")
    for _ in tqdm(range(total_frames), desc="Guard Processing Video"):
        ret, frame = cap.read()
        if not ret: break
        # FIXED: Removed 'render_logs' mismatch to comply with the master tracking algorithm
        proc_frame = process_single_frame(frame, fence_x_pos=450)
        out.write(proc_frame)

    cap.release()
    out.release()
    print(f"✅ Video processing complete! Output saved as: '{out_path}'")
    files.download(out_path)

def process_universal_image(file_name):
    img_pil = Image.open(file_name)
    orig_frame = cv2.cvtColor(np.array(img_pil), cv2.COLOR_RGB2BGR)
    h, w, _ = orig_frame.shape

    crop_name = detect_accurate_crop_by_layout(orig_frame)

    if "WHEAT FIELD" in crop_name:
        slider = widgets.IntSlider(value=int(w * 0.45), min=0, max=w, step=5, description='Fence Line X:')
        ui_output = widgets.Output()

        def on_value_change(change):
            with ui_output:
                clear_output(wait=True)
                test_frame = orig_frame.copy()
                processed = process_single_frame(test_frame, fence_x_pos=change['new'])
                plt.figure(figsize=(12, 10))
                plt.imshow(cv2.cvtColor(processed, cv2.COLOR_BGR2RGB))
                plt.axis('off')
                plt.show()

        slider.observe(on_value_change, names='value')
        display(slider, ui_output)

        with ui_output:
            processed = process_single_frame(orig_frame.copy(), fence_x_pos=slider.value)
            plt.figure(figsize=(12, 10))
            plt.imshow(cv2.cvtColor(processed, cv2.COLOR_BGR2RGB))
            plt.axis('off')
            plt.show()
    else:
        processed = process_single_frame(orig_frame.copy())
        plt.figure(figsize=(12, 10))
        plt.imshow(cv2.cvtColor(processed, cv2.COLOR_BGR2RGB))
        plt.axis('off')
        plt.show()

    return np.zeros((h, w), dtype=np.uint8)

